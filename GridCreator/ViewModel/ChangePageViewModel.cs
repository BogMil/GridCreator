using GridCreator.Commands;
using GridCreator.Model;
using GridCreator.View;
using GridCreator.View.Pages;
using System;
using System.Windows;
using System.Windows.Input;

namespace GridCreator.ViewModel
{
    class ChangePageViewModel
    {
        public ICommand GoToNextPageCommand { get; set; }
        public ICommand GoToPreviousPageCommand { get; set; }
        
        public ICommand HandleButtonVisibilityCommand { get; set; }
        public ICommand InsertAutoGeneratedCodeCommand { get; set; }
        public ICommand SetAddColumnOptionCommand { get; set; }

        private JqGridModel _jqGridModel;
        private GridCreatorWindow _baseWindowInstance;

        public ChangePageViewModel()
        {
            _baseWindowInstance = GridCreatorWindow.Instance;
            _jqGridModel = JqGridModelSingletonFactory.Instance;

            GoToNextPageCommand = new GoToNextPageCommand(_baseWindowInstance);
            GoToPreviousPageCommand = new GoToPreviousPageCommand(_baseWindowInstance);
            InsertAutoGeneratedCodeCommand = new CustomCommand(InsertAutoGeneratedCode, CanInsertAutoGeneratedCode);
            HandleButtonVisibilityCommand = new HandleButtonsVisibilityCommand(_baseWindowInstance);
            SetAddColumnOptionCommand = new SetAddColumnOptionCommand(_jqGridModel);

        }


        public bool CanInsertAutoGeneratedCode(object parameter)
        {
            return true;
        }

        public void InsertAutoGeneratedCode(object parameter)
        {
            var ShowWizardForGridCreationInstance = ShowGridCreatorWindow.Instance;
            ShowWizardForGridCreationInstance.test();
        }
    }
}
